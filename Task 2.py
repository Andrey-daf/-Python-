salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
months = 10  # Количество месяцев, которое планируется протянуть без долгов
increase = 0.03  # Ежемесячный рост цен

# TODO Рассчитайте подушку безопасности, чтобы протянуть 10 месяцев без долгов
# Инициализируем переменную для подушки безопасности
money_needed = 0

# Цикл по месяцам
for month in range(months):
    # В первый месяц расходы равны spend, затем увеличиваются на 5% каждый месяц
    if month > 0:
        spend *= (1 + increase)

    # Рассчитываем дефицит
    deficit = spend - salary

    # Если дефицит есть, то добавляем его к подушке безопасности
    if deficit > 0:
        money_needed += deficit

# Округляем результат до целого числа
money_needed = round(money_needed)

print(f"Подушка безопасности, чтобы протянуть {months} месяцев без долгов: {money_needed}")
